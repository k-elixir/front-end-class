// Objects are variables
// Objects can contain many values.
// The values are written as key:value pairs (properties).

let person = {
  firstName: "Kimia",
  lastName: "Rafi",
  age: 23,
};
console.log(person);

person["firstName"] = "Kim";
person.age = 25;
console.log(person);

person["city"] = "Oslo";
person.country = "Iran";
console.log(person);
